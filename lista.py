# LISTA
nomes = ["ana", "marcos", "bruno"]
print(f"listagem de nomes {nomes}")
# USANDO (APPEND) (incluir um novo item na variavel)
nomes.append("joao")

print(f"listagem e nomes ATUALIZADOS {nomes}")

# USANDO (REMOVE) (excuir um item na variavel )
nome_qualquer = input("digite um nome: ")
nomes.remove(nome_qualquer)
print(f"nova listagem de usuarios ATUALIZADOS (com exclusao) {nomes}")
# USANDO (SORT) (ordenar a lista)
nomes.sort()
print(f"listagem de nomes ORDENADAS {nomes}")
# USANDO (LEN) - (contar a quantidade de itens na lista)
l = len(nomes)
print(f"a lista tem {l} itens")


# append - incluir
# remove - excluir
# sort - colocar em ordem
# len - contar
